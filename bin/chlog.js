#!/usr/bin/env node
'use strict';
/*
 * chlog - generate a clean CHANGELOG.md from conventional commits + git tags.
 * Zero dependencies. Zero AI.
 */
const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const useColor = process.stdout.isTTY && !process.env.NO_COLOR;
const C = (c) => (s) => (useColor ? `\x1b[${c}m${s}\x1b[0m` : String(s));
const dim = C('2'), green = C('32'), cyan = C('36'), bold = C('1');

const TYPES = {
  feat: 'Features',
  fix: 'Bug Fixes',
  perf: 'Performance',
  refactor: 'Refactoring',
  docs: 'Documentation',
  build: 'Build System',
  ci: 'CI',
  test: 'Tests',
  style: 'Styles',
  chore: 'Chores',
  revert: 'Reverts',
};
const ORDER = Object.keys(TYPES);

function git(args, cwd) {
  return execFileSync('git', args, { cwd, encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 }).trim();
}

// Parse `type(scope)!: subject` -> structured commit, or null if non-conventional.
function parseSubject(subject) {
  const m = subject.match(/^(\w+)(?:\(([^)]+)\))?(!)?:\s*(.+)$/);
  if (!m) return { type: 'other', scope: null, breaking: false, subject: subject.trim() };
  return { type: m[1].toLowerCase(), scope: m[2] || null, breaking: !!m[3], subject: m[4].trim() };
}

// Returns [{ version, date, commits:[{hash,...}] }] newest-first.
function collect(cwd, repoUrl) {
  // tag -> date map
  const tagLines = (() => {
    try {
      return git(['tag', '--sort=-creatordate', '--format=%(refname:short)|%(creatordate:short)'], cwd)
        .split('\n').filter(Boolean);
    } catch { return []; }
  })();
  const tags = tagLines.map((l) => { const [name, date] = l.split('|'); return { name, date }; });

  const raw = git(['log', '--pretty=%H\x1f%s\x1f%d', '--no-merges'], cwd);
  const lines = raw ? raw.split('\n') : [];

  const tagByName = new Map(tags.map((t) => [t.name, t]));
  const sections = [];
  let current = { version: 'Unreleased', date: null, commits: [] };
  sections.push(current);

  for (const line of lines) {
    const [hash, subject, refs] = line.split('\x1f');
    // a commit may carry one or more tag refs -> start a new version section
    const tagMatch = refs && refs.match(/tag:\s*([^,\)]+)/g);
    if (tagMatch) {
      const name = tagMatch[0].replace(/tag:\s*/, '').trim();
      const meta = tagByName.get(name);
      current = { version: name, date: meta ? meta.date : null, commits: [] };
      sections.push(current);
    }
    const c = parseSubject(subject || '');
    current.commits.push({ hash: hash.slice(0, 7), full: hash, ...c, repoUrl });
  }
  return sections.filter((s) => s.commits.length);
}

function groupByType(commits) {
  const groups = {};
  const breaking = [];
  for (const c of commits) {
    if (c.breaking) breaking.push(c);
    (groups[c.type] = groups[c.type] || []).push(c);
  }
  return { groups, breaking };
}

function commitLine(c) {
  const scope = c.scope ? `**${c.scope}:** ` : '';
  const link = c.repoUrl ? ` ([\`${c.hash}\`](${c.repoUrl}/commit/${c.full}))` : ` (\`${c.hash}\`)`;
  return `- ${scope}${c.subject}${link}`;
}

function renderSection(section, opts) {
  const L = [];
  const header = section.version === 'Unreleased'
    ? '## [Unreleased]'
    : `## ${section.version}${section.date ? ` - ${section.date}` : ''}`;
  L.push(header, '');
  const { groups, breaking } = groupByType(section.commits);

  if (breaking.length) {
    L.push('### BREAKING CHANGES', '');
    for (const c of breaking) L.push(commitLine(c));
    L.push('');
  }
  for (const type of ORDER) {
    if (!groups[type]) continue;
    L.push(`### ${TYPES[type]}`, '');
    for (const c of groups[type]) L.push(commitLine(c));
    L.push('');
  }
  if (!opts.hideOther && groups.other) {
    L.push('### Other', '');
    for (const c of groups.other) L.push(commitLine(c));
    L.push('');
  }
  return L.join('\n');
}

function render(sections, opts) {
  const out = ['# Changelog', '',
    'All notable changes to this project are documented here.',
    'Generated by [chlog](https://github.com/bobfromarcher/chlog) from conventional commits.', ''];
  const chosen = opts.unreleased ? sections.filter((s) => s.version === 'Unreleased') : sections;
  for (const s of chosen) {
    // skip empty unreleased section unless explicitly asked
    if (s.version === 'Unreleased' && !s.commits.length) continue;
    out.push(renderSection(s, opts));
  }
  return out.join('\n').replace(/\n{3,}/g, '\n\n').trim() + '\n';
}

function detectRepoUrl(cwd) {
  try {
    let url = git(['config', '--get', 'remote.origin.url'], cwd);
    url = url.replace(/^git@([^:]+):/, 'https://$1/').replace(/\.git$/, '');
    return url.startsWith('http') ? url : null;
  } catch { return null; }
}

const HELP = `
chlog - generate a clean CHANGELOG.md from conventional commits + git tags.

Usage:
  chlog [path] [options]

Options:
  --write             Write CHANGELOG.md in the target directory
  --unreleased        Only show commits since the latest tag
  --hide-other        Omit commits that don't follow conventional format
  --json              Output structured JSON
  -h, --help          Show help
  -v, --version       Show version

Examples:
  chlog                    # print the full changelog
  chlog --write            # write/overwrite CHANGELOG.md
  chlog --unreleased       # what would the next release note say?
`;

function parseArgs(argv) {
  const o = { cwd: process.cwd(), write: false, unreleased: false, hideOther: false, json: false };
  const rest = [];
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--write') o.write = true;
    else if (a === '--unreleased') o.unreleased = true;
    else if (a === '--hide-other') o.hideOther = true;
    else if (a === '--json') o.json = true;
    else if (a === '-h' || a === '--help') o.help = true;
    else if (a === '-v' || a === '--version') o.version = true;
    else rest.push(a);
  }
  if (rest[0]) o.cwd = path.resolve(rest[0]);
  return o;
}

function main() {
  const o = parseArgs(process.argv.slice(2));
  if (o.help) { process.stdout.write(HELP); return; }
  if (o.version) { process.stdout.write(require('../package.json').version + '\n'); return; }
  try { git(['rev-parse', '--is-inside-work-tree'], o.cwd); }
  catch { process.stderr.write(`chlog: not a git repository: ${o.cwd}\n`); process.exit(1); }

  const repoUrl = detectRepoUrl(o.cwd);
  const sections = collect(o.cwd, repoUrl);
  if (o.json) { process.stdout.write(JSON.stringify(sections, null, 2) + '\n'); return; }
  const md = render(sections, o);
  if (o.write) {
    const file = path.join(o.cwd, 'CHANGELOG.md');
    fs.writeFileSync(file, md);
    const releases = sections.filter((s) => s.version !== 'Unreleased').length;
    process.stdout.write(`${green('✓')} wrote ${bold('CHANGELOG.md')} ${dim(`(${releases} release(s), ${sections.reduce((n, s) => n + s.commits.length, 0)} commits)`)}\n`);
    return;
  }
  process.stdout.write(md);
}

if (require.main === module) main();

module.exports = { parseSubject, groupByType, renderSection, render, parseArgs };
